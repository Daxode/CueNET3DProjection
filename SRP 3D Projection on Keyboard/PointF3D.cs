using System;
using System.Collections.Generic;
using System.Text;

namespace SRP_3D_Projection_on_Keyboard {
    class PointF3D {
        public float x = 0;
        public float y = 0;
        public float z = 0;

        public PointF3D(float x = 0.0f, float y = 0.0f, float z = 0.0f) {
            this.x = x;
            this.y = y;
            this.z = z;
        }
    }
}